package com.xplus.commons.thread.senior.threadpattern.masterworker;

public class MyData {

  private int id;
  private String name;
  private int data;

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getData() {
    return data;
  }

  public void setData(int data) {
    this.data = data;
  }

}
